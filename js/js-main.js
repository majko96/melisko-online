function sendEmail() {
  var name = $("#name");
  var email = $("#email");
  var subject = $("#email");
  var body = $("#message");

  if (isNotEmpty(name) && isNotEmpty(email) && isNotEmpty(body)) {
    $.ajax({
      url: 'sendEmail.php',
      method: 'POST',
      dataType: 'json',
      data: {
        name: name.val(),
        email: email.val(),
        subject: email.val(),
        body: body.val()
      },
      success: function(response) {
        if (response.status == "success")
          $("#ChatFrom")[0].reset(),
          alert('Vaša správa bola odoslaná!');
        else {
          alert('Správu sa nepodarilo odoslať, skúste to prosím znovu.');
        }
      }
    });
  }
}

function isNotEmpty(caller) {
  if (caller.val() == "") {
    caller.css('border', '1px solid red');
    return false;
  } else
    caller.css('border', '');
  return true;
}
